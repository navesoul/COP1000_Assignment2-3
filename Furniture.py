# Furniture.py - this program calculates the profit of a furniture item

itemName = "TV Stand"
retailPrice = 325.00
wholesalePrice = 200.00
profit = retailPrice - wholesalePrice
salePrice = retailPrice *0.25
saleprofit = salePrice - wholesalePrice
